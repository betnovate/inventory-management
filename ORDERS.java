    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
           try{ Class.forName("com.mysql.cj.jdbc.Driver");
      con = DriverManager.getConnection("jdbc:mysql://localhost:3306/MYINVENTORY","root","");
      Statement st= con. createStatement();
      String sql="select* from orders";
      PreparedStatement pat = con.prepareStatement(sql);
      ResultSet rs = pat.executeQuery();
      DefaultTableModel tm =(DefaultTableModel)jTable1.getModel();
      tm.setRowCount(0);
      while(rs.next()){
        Object o[]= {rs.getString("ID"),rs.getString("PRODUCTNAME"),rs.getString("BRAND"),rs.getString("SUPPLIER"),rs.getString("QUANTITY")}; 
        
      tm.addRow(o);
            
      }
      
      }catch(Exception ex)
      {JOptionPane.showMessageDialog(null, ex);}
    }//GEN-LAST:event_jButton6ActionPerformed

