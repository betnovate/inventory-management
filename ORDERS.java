       try {
            Class.forName("com.mysql.cj.jdbc.Driver");
           con = DriverManager.getConnection("jdbc:mysql://localhost:3306/MYINVENTORY","root","");
            String sql= "insert into orders values(?,?,?,?,?)";
            
                                                                                                  
                                                                                           
           PreparedStatement pat = con.prepareStatement(sql);
            pat.setString(1,txtid.getText());
            pat.setString(2,txtproname.getText());
            pat.setString(3,txtbrand.getText());
            pat.setString(4,txtsupp.getText());
            pat.setString(5,txtquan.getText());
             pat.executeUpdate();
            JOptionPane.showMessageDialog(this, "ORDER Added");
            con.close();
           
            txtid.setText("");
            txtproname.setText("");
            txtbrand.setText("");
            txtsupp.setText("");
            txtquan.setText("");
            txtid.requestFocus();
            
            }  
        catch (ClassNotFoundException ex)
        {
         Logger.getLogger(ORDERS.class.getName()).log(Level.SEVERE, null, ex);   
        }
        catch (SQLException ex) {
            Logger.getLogger(ORDERS.class.getName()).log(Level.SEVERE, null, ex);
        }
